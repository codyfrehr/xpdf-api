package io.xpdftools.pdftext;

/**
 * Defines the encoding to use for text output.
 *
 * @author Cody Frehr
 * @since 4.4.0
 */
public enum PdfTextEncoding {

    /**
     * @since 4.4.0
     */
    ASCII_7,

    /**
     * @since 4.4.0
     */
    LATIN_1,

    /**
     * @since 4.4.0
     */
    SYMBOL,

    /**
     * @since 4.4.0
     */
    UCS_2,

    /**
     * @since 4.4.0
     */
    UTF_8,

    /**
     * @since 4.4.0
     */
    ZAPF_DINGBATS,

}
